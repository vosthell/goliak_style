    void cargarDataProveedor()
    {
        ArrayList<clsProveedor> dataProveedor = objProveedor.consultaDataProveedor(txtCodigo.getText().toString()); 
        if(dataProveedor.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "No existe Proveedor", "Atención!", JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            codigoProveedor = dataProveedor.get(0).getIdProveedor();
            txtNombre.setText(dataProveedor.get(0).getNombre());
            txtDireccion.setText(dataProveedor.get(0).getDireccion());
            txtTelefono.setText(dataProveedor.get(0).getTelefono());

            //codigoCliente = dataCliente.get(0).getCodigo();
        }
    }
    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        
            exito = objProveedor.eliminarProveedor(codigoProveedor);
            if (exito)
            {
                JOptionPane.showMessageDialog(this, "Datos eliminados con éxito", "Atención!", JOptionPane.INFORMATION_MESSAGE);                
                objAuditoria.insertarAuditoria("frmProveedorDel", "ELIMINO PROVEEDOR: "
                                        + txtCodigo.getText().toUpperCase().toString()
                                        + " - " + txtNombre.getText().toUpperCase().toString(), "3");
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Error al ingresar la información", "Atención!", JOptionPane.ERROR_MESSAGE);
                objAuditoria.insertarAuditoria("frmProveedorDel", "INTENTÓ ELIMINAR PROVEEDOR: "
                                        + txtCodigo.getText().toUpperCase().toString()
                                        + " - " + txtNombre.getText().toUpperCase().toString(), "3");
            }
            dispose();        
    }//GEN-LAST:event_btnModificarActionPerformed

